#include<stdio.h>
int main(){
    float a,b;
    printf("Enter first number: ");
    scanf("%f",&a);
    printf("Enter second number: ");
    scanf("%f",&b);
    printf("Sum of numbers is %f\n",a+b);
    printf("Difference of numbers is %f\n",a-b);
    printf("Product of numbers is %f\n",a*b);
    printf("Division of numbers is %f\n",a/b);
    return 0;
}